import Foundation
import UIKit

extension UIColor {
    
    open static var outlookBlue: UIColor {
        return UIColor(red: 17/255, green: 92/255, blue: 186/255, alpha: 1.0)
    }
    
    open static var outlookLightBlue: UIColor {
        return UIColor(red: 17/255, green: 92/255, blue: 186/255, alpha: 0.2)
    }
    
    open static var outlookUltraLight: UIColor {
        return UIColor(red: 245/255, green: 245/255, blue: 245/255, alpha: 1.0)
    }
}
